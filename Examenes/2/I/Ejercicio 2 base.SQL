CREATE DATABASE PARCIAL2_EJ2
GO
USE PARCIAL2_EJ2
GO
CREATE TABLE CIUDADES(
  IDCIUDAD BIGINT NOT NULL PRIMARY KEY,
  CIUDAD VARCHAR(50) NOT NULL
)
GO
CREATE TABLE CAMIONES(
  IDCAMION VARCHAR(6) NOT NULL PRIMARY KEY,
  ANIO_PATENTAMIENTO INT NOT NULL,
  PESO_MAXIMO INT NOT NULL
)
GO
CREATE TABLE VIAJES(
  IDVIAJE INT NOT NULL PRIMARY KEY,
  IDCAMION VARCHAR(6) NOT NULL FOREIGN KEY REFERENCES CAMIONES(IDCAMION),
  IDCIUDAD BIGINT NOT NULL FOREIGN KEY REFERENCES CIUDADES(IDCIUDAD),
  FECHAHORA DATETIME NOT NULL
)
GO
CREATE TABLE ENCOMIENDAS(
  IDPAQUETE BIGINT NOT NULL PRIMARY KEY,
  IDVIAJE INT NOT NULL FOREIGN KEY REFERENCES VIAJES(IDVIAJE),
  PESO INT NOT NULL,
  COSTO SMALLMONEY NOT NULL
)
